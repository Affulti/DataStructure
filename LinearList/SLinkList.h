//静态链表: 借助数组来描述线性表的链式存储结构
#define ElemType int
#define MaxSize 50  //静态链表的最大长度

//--------------------------------类型描述--------------------------------
typedef struct {    //静态链表结构类型的定义
    ElemType data;  //存储数据元素
    int next;       //下一个元素的数组下标、以next == -1作为结束的标志
}SLinkList[MaxSize];

